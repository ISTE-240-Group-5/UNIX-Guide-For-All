<h2 class="subheader"> How to use Unix </h2>

<p class="text">
Commands are typed directly into the terminal and executed by pressing the enter/return key. Commands are case sensitive and must follow a specific syntax.  Syntax is the way that a command must be written in order to  function. Most commands have additional options that can be added after the command to change the way they function.  For example, given the “ls” command, an additional “-l” may be typed (separated by a space) in order to provide a more detailed description of the directory.  Some commands will require additional information, such as the name of the file you would like to perform the command on.  More detailed information on each command can be found under the commands section.

</p>

