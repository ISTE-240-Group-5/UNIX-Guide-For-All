<h3 class="subsubheader">rm Command</h3>

<p class="text">Removes the specified file. Can be used to delete
directories using options. Deleted items are not  recoverable.
</p>

  <h4 class="subsub" >Options</h4>

  <p class="text">
    -i: asks for confirmation before deleting the file
    -r: deletes the specified file. If a directory is specified, deletes all
    files and subdirectories (use with caution).
    -f: overrides confirmation when deleting write-protected files.
  </p>

<div class="terminals">
<p> <span style="color:#00FF85">example.bot:</span><span style="color:#00F0FF">~ $</span>
 cd </p>
        </div>


<p class="text">Syntax: rm [option] filename</p>

<h4 class="subsub" >Example 1: rm -r Stuff</h4>

<p class="text">Removes the directory named “Stuff” along with all of its contents.</p>


