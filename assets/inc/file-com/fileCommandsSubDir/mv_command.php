<h3 class="subsubheader">mv Command</h3>

<p class="text">Moves the file to the specified location. Can also be used to
  rename a file if the destination is a filename instead of a directory.
</p>

  <h4 class="subsub" >Options</h4>

  <p class="text">
    -i: asks for permission before overwriting a file
    -f: forces the move
  </p>


<p class="text">Syntax: mv [option] filename destination</p>

<h4 class="subsub" >Example 1:  mv -f example  /usr</h4>

<p class="text">Forces the move of example to directory “usr” </p>

<h4 class="subsub" >Example 2: mv oldfile  newfile </h4>

<p class="text">Renames “oldfile ” to “newfile ”</p>

