<h3 class="subsubheader">date command</h3>

<p class="text">prints the current date</p>

  <h4 class="subsub" >Options</h4>

  <p class="text">
    -u: displays time in UTC time zone
    --date: displaying previous dates
  </p>


  <h4 class="subsub" >Operators</h4>

  <p class="text">
    >    Displays the date and time to the user.

    >>  This can also be changed to display other dates/times.

  </p>




<div class="terminals">
<p> <span style="color:#00FF85">example.bot:</span><span style="color:#00F0FF">~ $</span>
 date </p>
        </div>


<p class="text">Syntax: date [option] [+format]</p>

<h4 class="subsub" >Example 1: date</h4>

<p class="text">Output: Tue December 2nd 7:00:00 PDT 2021</p>

<h4 class="subsub" >Example 2: date --date "1 year ago"</h4>

<p class="text">Output: Tue December 2nd 7:00:00 PDT 2020</p>



</div>
