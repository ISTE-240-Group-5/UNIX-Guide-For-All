<h3 class="subsubheader">grep Command</h3>

<p class="text">Find words or sentences in files or text</p>

  <h4 class="subsub" >Options</h4>

  <p class="text">
    -c : Print line count
    -h : Show matching lines
    -i : Case insensitive matching
    -l : Show filename only
    -n : Show matching lines and line numbers
    -v : Show lines that do not match search
    -w : Show only matches that include the whole word
    -o : Show only matching text
  </p>





