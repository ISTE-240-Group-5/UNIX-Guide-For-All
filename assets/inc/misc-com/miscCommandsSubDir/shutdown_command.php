<h3 class="subsubheader">Shutdown Command</h3>

<p class="text">Shuts down the machine</p>

  <h4 class="subsub" >Options</h4>

  <p class="text">
    -r: restart system
    -h: halt system processes
  </p>


<div class="terminals">
<p> <span style="color:#00FF85">example.bot:</span><span style="color:#00F0FF">~ $</span>
 shutdown</p>
        </div>


<p class="text">Syntax: shutdown [option]</p>

<h4 class="subsub" >Example 1: shutdown</h4>

<p class="text">Turns off the machine</p>

<h4 class="subsub" >Example 2: shutdown -r> file3</h4>

<p class="text">restarts the machine</p>



